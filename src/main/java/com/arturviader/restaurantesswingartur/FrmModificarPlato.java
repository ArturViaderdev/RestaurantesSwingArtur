    /**
     * Muestra el plato
     * @param queplato 
     */
    private void muestraplato(int queplato)
    {
      
        tNombre.setText(platos.get(queplato).getNombre());
            
        cRestaurante.setSelectedIndex(dimePosRestaurante(platos.get(queplato).getRestaurante()));
        
        cTipo.setSelectedIndex(platos.get(queplato).getTipo());
        
        tPrecio.setValue(platos.get(queplato).getPrecio());
        
        tNota.setValue(platos.get(queplato).getNota());
        
        tFecha.setText(Metodos.fechaaString(platos.get(queplato).getFecha()));
       
    }

    /**
     * Dice la posición en la lista de un restaurante buscando por el nombre
     * @param restaurante Nombre del restaurante
     * @return 
     */
    private int dimePosRestaurante(String restaurante)
    {
        boolean sal = false;
        boolean encontrado = false;
        int cont = 0;
        while(!sal)
        {
            if(restaurantes.get(cont).getNombre().equals(restaurante))
            {
                encontrado = true;
                sal = true;
            }
            else
            {
                cont+=1;
                if(cont>=restaurantes.size())
                {
                    sal = true;
                }
            }
        }
        if(encontrado)
        {
            return cont;
        }
        else
        {
            return -1;
        }
    }
    
