    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        // TODO add your handling code here:
        Boolean hacer = true;
        if(hayplatos && cPlato.getSelectedIndex()>=0 && seleccionado)
        {
            if(!(nombreantiguo.equals(tNombre.getText())))
            {
                if(Metodos.buscaPlato(platos,tNombre.getText()))
                {
                    JOptionPane.showMessageDialog(this, "Ya existe un plato con este nombre.", "Error", JOptionPane.ERROR_MESSAGE);
                    hacer = false;
                }
            }
            //Se actualiza el plato de la lista
            
            if(hacer){
                platos.get(cPlato.getSelectedIndex()).setNombre(tNombre.getText());
                platos.get(cPlato.getSelectedIndex()).setRestaurante(cRestaurante.getSelectedItem().toString());
                platos.get(cPlato.getSelectedIndex()).setTipo(cRestaurante.getSelectedIndex());
                platos.get(cPlato.getSelectedIndex()).setPrecio((Integer) tPrecio.getValue());
                platos.get(cPlato.getSelectedIndex()).setNota((Integer) tNota.getValue());
                platos.get(cPlato.getSelectedIndex()).setFecha(new Date());
                //Se reescribe el fichero
                Metodos.regrabaFicheroPlatos(platos);
                this.dispose();
            }
            
        }
    }//GEN-LAST:event_bAceptarActionPerformed

